package _03_if;

public class _02_Switch {
	public static void main(String[] args) {
		//switch ~ case ~ default
		int select = 3;
		switch(select) {
		case 1 :
			System.out.println(1);
			break;
		case 2 :
			System.out.println(2);
			break;
		case 3 :
			System.out.println(3);
			break;
		case 4 :
			System.out.println(4);
			break;	
		default :
			System.out.println("nothing");
			break;
		}//switch
	}//main
}//class
